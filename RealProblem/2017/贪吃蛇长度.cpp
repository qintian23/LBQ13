#include <iostream>
using namespace std;
int main(int argc, char* argv[])
{
    int ans=0;
    char c;
    while (true)
    {
        cin>> c;
        if(c=='#' || c=='H' || c=='T')
            ans+=1;
        else if(c=='@')
            ans+=2;
        
        if(c=='?')
            break;
    }
    cout<< ans <<endl;
    return 0;
}
